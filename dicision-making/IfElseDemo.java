class IfElseDemo {
    public static void main(String[] args) {
        int x = 30025;
        if(x%2==0){
            System.out.println(x+ " IS EVEN.");
        } else{
            System.out.println(x+" IS ODD.");
        }

        int year = 2024;
        if(year%4==0){
            System.out.println("LEAP YEAR");
        }else{
            System.out.println("NOT A LEAP YEAR");
        }
    }
}
