public class BitwiseOperators {
    public static void main(String[] args) {
        int x = 3;
        int y = 5;

        System.out.println("BITWISE AND");
        System.out.println(x&y);
        x = 2;
        y = 5;
        System.out.println(x&y);

        System.out.println("BITWISE OR");
        x = 3;
        y = 10;
        System.out.println(x|y);

        x = 2;
        y = 5;
        System.out.println(x|y);
    }
}
