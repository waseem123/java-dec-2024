class UnaryOperators {
    public static void main(String[] args) {
        int n = 100;
        n++; // n = n + 1;
        System.out.println(n);
        n--;
        System.out.println(n);
        System.out.println("_________");
        n = 100;

        System.out.println(++n);
        System.out.println(n++);
        System.out.println(n);

        System.out.println(n--);
        System.out.println(--n);
    }    
}


