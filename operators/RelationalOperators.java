class RelationalOperators{
    public static void main(String[] args) {
        int x = 100;
        int y = 100;

        System.out.println(x<y);
        System.out.println(y<x);
        System.out.println(x<=y);
        System.out.println(y<=x);

        System.out.println(x>=y);
        System.out.println(y>=x);
        System.out.println(x>y);
        System.out.println(y>x);

        System.out.println(x==y);
        System.out.println(x!=y);

    }
}