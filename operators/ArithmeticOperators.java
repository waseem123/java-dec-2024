class ArithmeticOperators {    
    public static void main(String[] args) {
        /* Division operation gives us quotient */
        int n1 = 100;
        int n2 = 5;
        int d = n1 / n2;
        System.out.println(d);

        /* Modulus Operations gives us remainder */
        int r = n1 % n2;
        System.out.println(r);
    }
}
