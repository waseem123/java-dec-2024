public class Operators {
    public static void main(String[] args) {
        int x = 20;
        int y = 5;
        int z = 88;

        System.out.println(x+y/z);
        System.out.println((x*y)/(x+20));
        System.out.println((x+y)*z);
        System.out.println(x+y*z-z);

        int marks = 125;
        System.out.println((marks*100)/300);
    }
}
